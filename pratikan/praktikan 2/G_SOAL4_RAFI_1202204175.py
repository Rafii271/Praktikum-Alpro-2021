hari = int(input("Masukkan nomor Hari : "))

if hari == 1:
    print(f"hari ke- {hari} adalah Hari Senin")
elif hari == 2:
    print(f"hari ke- {hari} adalah Hari Selasa")
elif hari == 3:
    print(f"hari ke- {hari} adalah Hari Rabu")
elif hari == 4:
    print(f"hari ke- {hari} adalah Hari Kamis")
elif hari == 5:
    print(f"hari ke- {hari} adalah Hari Jumat")
elif hari == 6:
    print(f"hari ke- {hari} adalah Hari Sabtu")
elif hari == 7:
    print(f"hari ke- {hari} adalah Hari Minggu")
else:
    print(f"Tidak Ada hari ke- {hari}")